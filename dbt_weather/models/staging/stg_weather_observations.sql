select
    city_id as id_cidade,
    city_name as cidade,
    country as pais,
    datetime as data_observacao,
    sunrise as nascer_sol,
    sunset as por_sol,
    weather_id as id_clima,
    weather_main as clima_principal,
    weather_description as descricao_clima,
    temperature as temperatura,
    feels_like as sensacao_termica,
    temp_min as temperatura_minima,
    temp_max as temperatura_maxima,
    pressure as pressao,
    humidity as umidade,
    sea_level as nivel_mar,
    grnd_level as nivel_solo,
    wind_speed as velocidade_vento,
    wind_deg as direcao_vento,
    wind_gust as rajada_vento,
    clouds as nebulosidade,
    visibility as visibilidade,
    timezone as fuso_horario,
    code as codigo_resposta,
    base as base_origem,
    longitude as longitude,
    latitude as latitude,
    sys_id as id_sistema,
    ingested_at as data_ingestao,
    source_name as fonte_dado
from {{ source('raw', 'weather_observations') }}
